package primeiro_programa;

public class Visibilidade {
    // usando "public" a variavel x fica visivel e poderá ser acessada em outras classes no mesmo escopo do projeto ou usando import para classes fora do escopo:
    public int x = 10;
    // usando "private" a variavel y só fica visivel para esta classe atual:
    private int y;
    // e usando nenhuma palavra a variavel z terá visibilidade "padrão" e poderá ser acessada em outras classes
    int z;
    }

